export type TranslationKey = keyof typeof translations.en;

export function normalizeLanguage(language?: string | null): Language {
  if (language === "pt" || language === "es") {
    return language;
  }

  return "en";
}

export function t(language: Language | string | null | undefined, key: TranslationKey, params?: Record<string, string | number>) {
  const normalized = normalizeLanguage(language);
  const dictionary = translations[normalized] as Partial<Record<TranslationKey, string>>;
  let text: string = dictionary[key] ?? translations.en[key];

  if (params) {
    for (const [name, value] of Object.entries(params)) {
      text = text.replaceAll(`{${name}}`, String(value));
    }
  }

  return text;
}

